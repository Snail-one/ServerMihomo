func TestDefaultMihomoConfigGeneratesRandomSecret(t *testing.T) {
	first, err := defaultMihomoConfig()
	if err != nil {
		t.Fatalf("defaultMihomoConfig() error = %v", err)
	}
	second, err := defaultMihomoConfig()
	if err != nil {
		t.Fatalf("defaultMihomoConfig() second error = %v", err)
	}

	secret := configLineValue(t, first, "secret: ")
	if secret == "set-your-secret" {
		t.Fatalf("secret should not use placeholder value")
	}
	if len(secret) != 100 {
		t.Fatalf("secret length = %d, want 100", len(secret))
	}
	if !isAlphaNumeric(secret) {
		t.Fatalf("secret should be alphanumeric, got %q", secret)
	}
	if secret == configLineValue(t, second, "secret: ") {
		t.Fatalf("secret should be regenerated for each default config")
	}
}

func TestValidateSubscriptionDataRequiresValidYAMLWithProxies(t *testing.T) {
	tests := []struct {
		name    string
		content string
		wantErr string
	}{
		{
			name: "valid",
			content: `proxies:
  - name: Node A
    type: ss
    server: example.com
    port: 443
`,
		},
		{
			name: "invalid yaml",
			content: `proxies:
  - name: Node A
    port: [`,
			wantErr: "订阅 YAML 无效",
		},
		{
			name: "missing proxies",
			content: `proxy-groups:
  - name: Proxies
`,
			wantErr: "缺少 proxies",
		},
		{
			name: "empty proxies",
			content: `proxies: []
`,
			wantErr: "proxies 不能为空",
		},
		{
			name: "proxy without name",
			content: `proxies:
  - type: ss
    server: example.com
    port: 443
`,
			wantErr: "没有可用 name",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			err := ValidateSubscriptionData([]byte(tt.content))
			if tt.wantErr == "" {
				if err != nil {
					t.Fatalf("ValidateSubscriptionData() error = %v", err)
				}
				return
			}
			if err == nil || !strings.Contains(err.Error(), tt.wantErr) {
				t.Fatalf("ValidateSubscriptionData() error = %v, want containing %q", err, tt.wantErr)
			}
		})
	}
}

func TestDownloadSubscriptionRejectsInvalidYAMLWithoutReplacingExistingFile(t *testing.T) {
	targetPath := filepath.Join(t.TempDir(), "subscription.yaml")
	if err := os.WriteFile(targetPath, []byte("existing\n"), 0o644); err != nil {
		t.Fatalf("WriteFile(existing) error = %v", err)
	}

	body := "not: [valid"
	client := &http.Client{
		Transport: roundTripFunc(func(req *http.Request) (*http.Response, error) {
			return &http.Response{
				StatusCode:    http.StatusOK,
				Status:        "200 OK",
				Body:          io.NopCloser(strings.NewReader(body)),
				ContentLength: int64(len(body)),
				Header:        make(http.Header),
				Request:       req,
			}, nil
		}),
	}
	req, err := http.NewRequestWithContext(context.Background(), http.MethodGet, "https://example.com/sub", nil)
	if err != nil {
		t.Fatalf("NewRequestWithContext() error = %v", err)
	}

	err = downloadSubscriptionWithClient(client, req, targetPath)
	if err == nil || !strings.Contains(err.Error(), "订阅 YAML 无效") {
		t.Fatalf("downloadSubscriptionWithClient() error = %v, want invalid YAML error", err)
	}

	data, err := os.ReadFile(targetPath)
	if err != nil {
		t.Fatalf("ReadFile(target) error = %v", err)
	}
	if string(data) != "existing\n" {
		t.Fatalf("target file was replaced: %q", data)
	}
	if _, err := os.Stat(targetPath + ".download"); !os.IsNotExist(err) {
		t.Fatalf("temporary download file should be removed, stat err = %v", err)
	}
}

type roundTripFunc func(*http.Request) (*http.Response, error)

func (f roundTripFunc) RoundTrip(req *http.Request) (*http.Response, error) {
	return f(req)
}

func writeTestFile(t *testing.T, targetPath string, content string) {
	t.Helper()
	if err := os.MkdirAll(filepath.Dir(targetPath), 0o755); err != nil {
		t.Fatalf("MkdirAll(%s) error = %v", filepath.Dir(targetPath), err)
	}
	if err := os.WriteFile(targetPath, []byte(content), 0o644); err != nil {
		t.Fatalf("WriteFile(%s) error = %v", targetPath, err)
	}
}

func assertTestFileContent(t *testing.T, targetPath string, want string) {
	t.Helper()
	data, err := os.ReadFile(targetPath)
	if err != nil {
		t.Fatalf("ReadFile(%s) error = %v", targetPath, err)
	}
	if string(data) != want {
		t.Fatalf("ReadFile(%s) = %q, want %q", targetPath, data, want)
	}
}

func configLineValue(t *testing.T, content string, prefix string) string {
	t.Helper()
	for _, line := range strings.Split(content, "\n") {
		if strings.HasPrefix(line, prefix) {
			return strings.TrimSpace(strings.TrimPrefix(line, prefix))
		}
	}
	t.Fatalf("expected config to contain line prefix %q\n%s", prefix, content)
	return ""
}

func isAlphaNumeric(value string) bool {
	for _, r := range value {
		if r >= 'a' && r <= 'z' {
			continue
		}
		if r >= 'A' && r <= 'Z' {
			continue
		}
		if r >= '0' && r <= '9' {
			continue
		}
		return false
	}
	return true
}

func assertContains(t *testing.T, value string, needle string) {
	t.Helper()
	if !strings.Contains(value, needle) {
		t.Fatalf("expected output to contain %q\n%s", needle, value)
	}
}

func assertBefore(t *testing.T, value string, first string, second string) {
	t.Helper()
	firstIndex := strings.Index(value, first)
	secondIndex := strings.Index(value, second)
	if firstIndex < 0 || secondIndex < 0 || firstIndex >= secondIndex {
		t.Fatalf("expected %q to appear before %q\n%s", first, second, value)
	}
}
