func TestApplySubscriptionMergesCustomBeforeSubscriptionAndGroups(t *testing.T) {
	store := Store{BaseDir: t.TempDir()}
	if err := store.EnsureDirs(); err != nil {
		t.Fatalf("EnsureDirs() error = %v", err)
	}

	writeTestFile(t, store.BaseConfigPath(), "mode: rule\n")
	writeTestFile(t, store.RulesPath(), `rules:
- PROCESS-NAME,custom.exe,Custom
- GEOIP,CN,GEOIP-CN,no-resolve
- MATCH,Final
`)
	writeTestFile(t, store.ProxyGroupsPath(), `proxy-groups:
- -Custom
- +Final
`)
	writeTestFile(t, store.CustomProxiesPath(), `proxies:
- {name: Custom One, server: 127.0.0.1, port: 1080, type: socks5}
`)

	subscriptionFile := "subscription-test.yaml"
	writeTestFile(t, store.ProfilePath(subscriptionFile), `proxies:
  - {name: 未知 SS-01 | free-nodes, server: 62.146.231.112, port: 12012, type: ss, cipher: chacha20-ietf-poly1305, password: secret}
  - name: Block Node
    server: example.com
    port: 443
    type: vmess
`)

	result, err := store.ApplySubscription(ApplyOptions{
		Subscription: Subscription{
			Name: "测试订阅",
			File: subscriptionFile,
			URL:  "https://example.com/sub",
		},
		GenerateProxyGroups: true,
	})
	if err != nil {
		t.Fatalf("ApplySubscription() error = %v", err)
	}

	finalData, err := os.ReadFile(store.FinalConfigPath())
	if err != nil {
		t.Fatalf("ReadFile(final config) error = %v", err)
	}
	final := string(finalData)

	assertContains(t, final, "proxies:\n  - {name: Custom One")
	assertContains(t, final, "  - {name: 未知 SS-01 | free-nodes")
	assertBefore(t, final, "Custom One", "未知 SS-01 | free-nodes")
	assertContains(t, final, "- name: Snail")
	assertContains(t, final, "  - \"Custom One\"")
	assertContains(t, final, "- name: Proxies")
	assertContains(t, final, "  - \"未知 SS-01 | free-nodes\"")
	assertContains(t, final, "- name: Custom")
	assertContains(t, final, "  - DIRECT")
	assertContains(t, final, "- name: Final")
	assertContains(t, final, "rules:\n- PROCESS-NAME,custom.exe,Custom")

	if result.ProxyCount != 2 {
		t.Fatalf("ProxyCount = %d, want 2", result.ProxyCount)
	}
	if result.CustomProxyCount != 1 {
		t.Fatalf("CustomProxyCount = %d, want 1", result.CustomProxyCount)
	}
	if result.GroupCount != 4 {
		t.Fatalf("GroupCount = %d, want 4", result.GroupCount)
	}
	if len(result.Warnings) != 1 || !strings.Contains(result.Warnings[0], "GEOIP-CN") {
		t.Fatalf("Warnings = %#v, want missing GEOIP-CN warning", result.Warnings)
	}
}

func TestCopySubscriptionToFinalConfigPreservesContent(t *testing.T) {
	store := Store{BaseDir: t.TempDir()}
	if err := store.EnsureDirs(); err != nil {
		t.Fatalf("EnsureDirs() error = %v", err)
	}

	subscriptionFile := "subscription-test.yaml"
	subscriptionContent := `mixed-port: 7890
proxies:
  - {name: Node A, server: example.com, port: 443, type: ss, cipher: chacha20-ietf-poly1305, password: secret}
proxy-groups:
  - name: Proxies
    type: select
    proxies:
      - Node A
rules:
  - MATCH,Proxies
`
	writeTestFile(t, store.ProfilePath(subscriptionFile), subscriptionContent)

	finalPath, err := store.CopySubscriptionToFinalConfig(Subscription{
		Name: "测试订阅",
		File: subscriptionFile,
		URL:  "https://example.com/sub",
	})
	if err != nil {
		t.Fatalf("CopySubscriptionToFinalConfig() error = %v", err)
	}
	if finalPath != store.FinalConfigPath() {
		t.Fatalf("finalPath = %q, want %q", finalPath, store.FinalConfigPath())
	}
	assertTestFileContent(t, store.FinalConfigPath(), subscriptionContent)
	assertTestFileContent(t, store.ProfilePath(subscriptionFile), subscriptionContent)
}

